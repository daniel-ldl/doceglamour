    private void jbsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbsalvarActionPerformed
       Conexao novaconexao = new Conexao();
       novaconexao.getConexao();
       String salvar = "INSERT INTO dados.sabor(nome_sabor) VALUES (?);";
       try{
           PreparedStatement smtSalvarSabor = novaconexao.getConexao().prepareStatement(salvar);
           smtSalvarSabor.setString(1, jtnome.getText());
           JOptionPane.showMessageDialog(null, "sabor salvo com Sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
           smtSalvarSabor.executeUpdate();
           CadastrarProduto.cbsabor.removeAllItems();
           CadastrarProduto.carregarcb();
           this.dispose();
       } catch (SQLException ex) {
            Logger.getLogger(CadastrarSabor.class.getName()).log(Level.SEVERE, null, ex);
        }
       finally{
           novaconexao.fecharConexao();
       }
    }//GEN-LAST:event_jbsalvarActionPerformed


