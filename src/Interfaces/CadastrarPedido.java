    private void Inicializar() {
        jtCliente.setEditable(false);
        jtcod.setEnabled(false);
        jtvalorentrega.setEnabled(false);
        jtsinal.setEnabled(false);
        jf_data_sinal.setEnabled(false);
        jtdesconto.setDocument(new TeclasPermitidas());
        jtvalortotal.setEditable(false);
        jtfinal.setEditable(false);
        @SuppressWarnings("UnusedAssignment")
        ResultSet rs = null;
        @SuppressWarnings("UnusedAssignment")
        ResultSet rscod = null;
        Conexao nova = new Conexao();
        nova.getConexao();
        String sqlcodfinal = "SELECT codigo_pedido FROM dados.pedido";
        String aux = "";
        try {

            PreparedStatement smtBuscaCodFinal = nova.getConexao().prepareStatement(sqlcodfinal);

            rscod = smtBuscaCodFinal.executeQuery();

            while (rscod.next()) {
                aux = rscod.getString("codigo_pedido");
            }
            if (aux.equals("")) {
                aux = "0";
            }
            codfinal = Integer.parseInt(aux);
            codfinal++;
            aux = Integer.toString(codfinal);
            jtcod.setText(aux);

        } catch (SQLException ex) {
            Logger.getLogger(CadastrarPedido.class.getName()).log(Level.SEVERE, null, ex);
        }
        ResultSet rs2 = null;
        String sql2 = "SELECT * from dados.produto order by nome;";
        try {

            PreparedStatement smtBuscaProduto = nova.getConexao().prepareStatement(sql2);
            rs2 = smtBuscaProduto.executeQuery();
            while (rs2.next()) {
                Produto dados = new Produto(rs2.getInt("codigo_produto"), rs2.getString("nome"), rs2.getString("descricao"), rs2.getFloat("valor"), rs2.getString("categoria"), rs2.getString("sabor"));
                novo.add(dados);
                cbproduto.addItem(rs2.getString("nome"));

            }

        } catch (SQLException ex) {
            Logger.getLogger(CadastrarPedido.class.getName()).log(Level.SEVERE, null, ex);
        } finally {

            nova.fecharConexao();
        }

    }

