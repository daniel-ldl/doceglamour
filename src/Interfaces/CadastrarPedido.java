    private void jtdescontoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtdescontoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (usuario.getNivel() == 2) {
                JOptionPane.showMessageDialog(null, "Operação válida somente para Administrador", "Atenção", JOptionPane.INFORMATION_MESSAGE);

            } else {
                float desconto = 0, valorFinal = 0, valorTotal = 0;
                if (jtdesconto.getText().equals("")) {
                    valorFinal = Float.parseFloat(jtvalortotal.getText().replace(",", "."));
                    jtfinal.setText(String.valueOf(valorFinal).replace(",", "."));

                } else {
                    desconto = Float.parseFloat(jtdesconto.getText().replace(",", "."));
                    valorTotal = Float.parseFloat(jtvalortotal.getText().replace(",", "."));
                    valorFinal = valorTotal - desconto;
                    jtfinal.setText(String.valueOf(valorFinal));
                }

            }

        }
    }//GEN-LAST:event_jtdescontoKeyPressed

