    private void jbadicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbadicionarActionPerformed
        Produto novoProduto = new Produto();
        float valor = 0;
        int quantidade;
        String quant;
        quant = jtquantidade.getText();
        quantidade = Integer.parseInt(quant);
        String produto = (String) cbproduto.getSelectedItem();
        Conexao nova = new Conexao();
        nova.getConexao();
        String selectValor = "SELECT * FROM dados.produto where nome = '" + produto + "'";
        ResultSet rs = null;
        try {
            PreparedStatement smtSelectValor = nova.getConexao().prepareStatement(selectValor);
            rs = smtSelectValor.executeQuery();
            while (rs.next()) {

                novoProduto.setNome(rs.getString("nome"));
                novoProduto.setQuantidade(quantidade);
                novoProduto.setValor(rs.getFloat("valor"));
                novoProduto.setValorFinal(novoProduto.getValor() * novoProduto.getQuantidade());
                valor = rs.getFloat("valor");
            }
            modelProduto.addRow(novoProduto);

            DecimalFormat decimal = new DecimalFormat("0.00");
            valor_total += valor * quantidade;
            jtvalortotal.setText(String.valueOf(decimal.format(valor_total)));
            jtfinal.setText(String.valueOf(decimal.format(valor_total)));
            
        } catch (SQLException ex) {
            Logger.getLogger(CadastrarPedido.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            nova.fecharConexao();
        }


    }//GEN-LAST:event_jbadicionarActionPerformed

